// Conditionals are block of statements that are executed only when the given condition is satified

// if statement
// An if statement only executes if the condition is true
if (true) {
    // evalutate
}

// if-else statement
// An if-else statement executes code in if-block when condition is true otherwise executes code in else-block
if (false) {
    // skip
} else {
    // evaluate
}


// if-else if-else statement
if (false) {
    // skip
} else if (true) {
    // evaluate
} else {
    // unreachable code
}


// ternary operator (?:)
// ternary operator is a shorthand operator for if-else statements
const a = (6 % 2) ? "odd" : "even"

