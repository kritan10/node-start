// Comparison operators are used to compare any two values

const a = 1
const b = true

// Equals (==) operator
// implicit type coercing
// returns true if both sides are equal
console.log(a == b);


// Strict Equals (===) operator
// no type coercing
// returns true if both sides are equal
console.log(a === b);

// Not Equals (!=) operator (inverse of ==)
// implicit type coercing
// returns true if both sides are unequal
console.log(a != b);


// Strict Not Equals (!==) operator (inverse of ===)
// no type coercing
// returns true if both sides are unequal
console.log(a !== b);

const num1 = 10
const num2 = 21

// Greater Than (>)
// used to compare numeric values
// returns true if the left hand is greater than right hand
console.log(num1 > num2);

// Greater Than or Equal To(>=)
// used to compare numeric values
// returns true if the left hand is greater than or equal to right hand
console.log(num1 >= num2);

// Less Than(<)
// used to compare numeric values
// returns true if the left hand is lesser than right hand
console.log(num1 < num2);

// Less Than or Equal To(<=)
// used to compare numeric values
// returns true if the left hand is lesser than or equal to right hand
console.log(num1 <= num2);
